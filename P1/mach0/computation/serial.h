/**
 * Compute arctan with serial machin 
 * @param unsigned long n machin number of computations
 * @param double x parameter for arctan
 * @return double arctan(x) computed with machin formula of n repetitions
 */
double machinArctanSerial(unsigned long n,double x);

/**
 * Compute pi from machin serial arctan
 * @param unsigned long n number of repetitions in machin arctan
 * @return approximation of pi computed with machin of n repetitions
 */
double piMachinSerial(unsigned long n);
