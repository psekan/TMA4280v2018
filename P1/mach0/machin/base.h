/**
 * Compute x^(2*i-1)
 * @param double x base
 * @param unsigned int i exponent
 * @return x^(2*i-1)
 */
double machinXPower(double x, unsigned long i);

/**
 * Compute v_i(x)
 * @param double x base
 * @param unsigned int i exponent
 * @return v_i(x)
 */
double machinPart(double x, unsigned long i);
